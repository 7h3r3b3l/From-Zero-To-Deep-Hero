# Custom Tensor
custom_tensor1 = torch.tensor([[1,3,3,7],[1,3,3,7],[1,3,3,7]])
custom_tensor2 = torch.tensor([[1,3,3,7],[2,3,3,7],[3,3,3,7]])

# Indexing and Slicing
first_line = custom_tensor1[0, 0:]
single_element = custom_tensor1[1,3]

## Operations

# e^tensor
torch.exp(custom_tensor1)

# Compare tensors element by element
comp = custom_tensor1 == custom_tensor2

# Sum the elements
sum1 = custom_tensor1.sum()
sum2 = custom_tensor2.sum()

# Sum
t_sum = custom_tensor1 + custom_tensor2

## Saving Memory

# Bad Way
X = custom_tensor2 
Y = custom_tensor1 
id_1 = id(X)
X = X+Y
id_2 = id(X)
id_1 == id_2 # False

# Good Way
X = custom_tensor2 
Y = custom_tensor1 
id_1 = id(X)
X[:] = X+Y # X[:] solves everything.
id_2 = id(X)
id_1 == id_2 # True

